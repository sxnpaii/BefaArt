let infoIcon1 = document.querySelector(".infoIcon1");
let infoItem1 = document.querySelector(".info.i__1");

let close1 = document.querySelector(".closeIcon1");
let hoverDiv1 = document.querySelector('.hover.h__1')



infoIcon1.addEventListener("click", function info_1(a) {
    a.preventDefault();
    hoverDiv1.classList.toggle("hidden");
    setTimeout(() => {
        infoItem1.classList.toggle("visible");
    }, 300);

});

close1.addEventListener("click", function infoCls(b) {
    b.preventDefault();
    hoverDiv1.classList.toggle("hidden");
    infoItem1.classList.toggle("visible");

});
